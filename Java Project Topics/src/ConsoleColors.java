public class ConsoleColors {
    // 리셋
    public static final String RESET = "\033[0m";  // 텍스트 리셋

    // 일반 색상
    public static final String BLACK = "\033[0;30m";   // 검정색
    public static final String RED = "\033[0;31m";     // 빨강색
    public static final String GREEN = "\033[0;32m";   // 초록색
    public static final String YELLOW = "\033[0;33m";  // 노란색
    public static final String BLUE = "\033[0;34m";    // 파란색
    public static final String PURPLE = "\033[0;35m";  // 보라색
    public static final String CYAN = "\033[0;36m";    // 청록색
    public static final String WHITE = "\033[0;37m";   // 흰색

    // 볼드 색상
    public static final String BLACK_BOLD = "\033[1;30m";  // 검정색 볼드
    public static final String RED_BOLD = "\033[1;31m";    // 빨강색 볼드
    public static final String GREEN_BOLD = "\033[1;32m";  // 초록색 볼드
    public static final String YELLOW_BOLD = "\033[1;33m"; // 노란색 볼드
    public static final String BLUE_BOLD = "\033[1;34m";   // 파란색 볼드
    public static final String PURPLE_BOLD = "\033[1;35m"; // 보라색 볼드
    public static final String CYAN_BOLD = "\033[1;36m";   // 청록색 볼드
    public static final String WHITE_BOLD = "\033[1;37m";  // 흰색 볼드
}
